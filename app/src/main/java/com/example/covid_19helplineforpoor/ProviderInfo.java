package com.example.covid_19helplineforpoor;

import java.util.Date;

public class ProviderInfo {
    private String name;
    private String date;

    public ProviderInfo(){}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
